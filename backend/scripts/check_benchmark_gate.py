import argparse
import json
from datetime import datetime, timezone
from pathlib import Path


def build_parser() -> argparse.ArgumentParser:
    parser = argparse.ArgumentParser(
        description="Enforce the benchmark governance gate before a release or threshold rollout.",
    )
    parser.add_argument(
        "threshold_report",
        type=Path,
        help="Path to a threshold governance report JSON generated by generate_threshold_report.py.",
    )
    parser.add_argument(
        "--allow-conditional",
        action="store_true",
        help="Treat a conditional report as passing the gate.",
    )
    parser.add_argument(
        "--max-age-days",
        type=int,
        default=30,
        help="Maximum age of the threshold report in days (default: 30).",
    )
    parser.add_argument(
        "--require-dataset-name",
        help="Optional dataset name that must match the report dataset.",
    )
    return parser


def evaluate_gate(
    report: dict,
    *,
    allow_conditional: bool,
    max_age_days: int,
    require_dataset_name: str | None,
) -> tuple[bool, list[str]]:
    failures: list[str] = []

    decision = report.get("decision", {})
    status = decision.get("status")
    if status == "go":
        pass
    elif status == "conditional" and allow_conditional:
        pass
    else:
        failures.append(f"decision_status={status}")

    generated_at_raw = report.get("generated_at")
    if generated_at_raw:
        generated_at = datetime.fromisoformat(generated_at_raw.replace("Z", "+00:00"))
        age_days = (datetime.now(timezone.utc) - generated_at).days
        if age_days > max_age_days:
            failures.append(f"report_too_old={age_days}d>{max_age_days}d")
    else:
        failures.append("missing_generated_at")

    dataset = report.get("dataset", {})
    if require_dataset_name and dataset.get("name") != require_dataset_name:
        failures.append(
            f"dataset_name_mismatch={dataset.get('name')}!=${require_dataset_name}".replace("$", "")
        )

    return (len(failures) == 0), failures


def main() -> int:
    parser = build_parser()
    args = parser.parse_args()
    report = json.loads(args.threshold_report.read_text(encoding="utf-8"))

    passed, failures = evaluate_gate(
        report,
        allow_conditional=args.allow_conditional,
        max_age_days=args.max_age_days,
        require_dataset_name=args.require_dataset_name,
    )

    if passed:
        print(
            f"Benchmark gate PASSED for dataset {report.get('dataset', {}).get('name')} "
            f"with decision {report.get('decision', {}).get('status')}."
        )
        return 0

    print("Benchmark gate FAILED:")
    for failure in failures:
        print(f"- {failure}")
    return 1


if __name__ == "__main__":
    raise SystemExit(main())
